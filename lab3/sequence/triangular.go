package sequence

// Task 1: Triangular numbers
//
// triangular(n) returns the n-th Triangular number, and is defined by the
// recurrence relation F_n = n + F_n-1 where F_0=0 and F_1=1
//
// Visualization of numbers:
// n = 1:    n = 2:     n = 3:      n = 4:    etc...
//
//	o         o          o           o
//	         o o        o o         o o
//	                   o o o       o o o
//	                              o o o o
func triangular(n uint) uint {
	t := uint(0)
	for i := uint(0); i <= n; i++ {
		t = i + t
	}
	return t
}
